export const getEventsByCity = (cityId: string): Event[] => {
  return events.filter((event) => event.cityId === cityId && event.status === 'published');
};

export const getEventById = (id: string): Event | undefined => {
  return events.find((event) => event.id === id);
};

export const getFeaturedEvents = (cityId: string): Event[] => {
  return getEventsByCity(cityId).slice(0, 4);
};
